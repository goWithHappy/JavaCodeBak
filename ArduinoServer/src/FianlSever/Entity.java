package FianlSever;

/**
 * 存储servletName以及url为解析web做准备
 * @author Admain
 *
 */
public class Entity {
	String servletName;
	String url;
	public String getServletName() {
		return servletName;
	}
	public void setServletName(String servletName) {
		this.servletName = servletName;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
}
